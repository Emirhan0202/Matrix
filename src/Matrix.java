    private static void clearMatrix(){
        for (int i = 0; i < matrix.length ; i++) {
            for (int j = 0; j < matrix[i].length; j++) {
                matrix[i][j] = ' ';
            }
        }
    }

    public static void printMatrix(String text){
        char[] buchstaben = text.toCharArray();
        int z = 0;
        for (char buchstabe:buchstaben) {
           char[][] charcterArray = map.get(buchstabe);

            for (int i = 0; i < charcterArray.length; i++) {
                for (int j = 0; j < charcterArray[i].length; j++) {
                   char raute = charcterArray[i][j];
                   matrix[i][j+z*7] = raute;
                }
            }
            z++;
        }
        for (int i = 0; i < matrix.length; i++) {
            for (int j = 0; j < matrix[i].length; j++) {
                System.out.print(matrix[i][j]);
            }
            System.out.println();
        }
        System.out.println();
    }
